#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const repo = process.cwd()
const issueDir = path.join(repo, 'issues/0004-complete-shadcn-parity-and-docs')
const outputPath = path.join(issueDir, 'parity-inventory.md')
const registryRoot = path.join(repo, 'vendor/shadcn-ui/apps/v4/registry/new-york-v4')

const uiRegistryPath = path.join(registryRoot, 'ui/_registry.ts')
const examplesRegistryPath = path.join(registryRoot, 'examples/_registry.ts')
const blocksRegistryPath = path.join(registryRoot, 'blocks/_registry.ts')
const chartsRegistryPath = path.join(registryRoot, 'charts/_registry.ts')
const radcnPackagePath = path.join(repo, 'radcn/packages/radcn/package.json')
const docsContentPath = path.join(repo, 'radcn/apps/docs/app/content/components.tsx')

const knownOutcomeSlugs = ['form', 'date-picker', 'data-table']

const [uiText, examplesText, blocksText, chartsText, packageText, docsText] = await Promise.all([
  readFile(uiRegistryPath, 'utf8'),
  readFile(examplesRegistryPath, 'utf8'),
  readFile(blocksRegistryPath, 'utf8'),
  readFile(chartsRegistryPath, 'utf8'),
  readFile(radcnPackagePath, 'utf8'),
  readFile(docsContentPath, 'utf8'),
])

const uiItems = parseRegistryItems(uiText)
const examples = parseRegistryItems(examplesText)
const blocks = parseRegistryItems(blocksText)
const charts = parseRegistryItems(chartsText)
const radcnExports = exportedSlugs(JSON.parse(packageText).exports)
const docsSlugs = docsRegistrySlugs(docsText)

const uiNames = uiItems.map((item) => item.name).sort()
const knownSlugs = Array.from(new Set([...uiItems.map((item) => item.name), ...radcnExports, ...knownOutcomeSlugs])).sort(
  (a, b) => b.length - a.length || a.localeCompare(b),
)
const exampleGroups = groupByDependency(examples, knownSlugs)
const missingUi = uiNames.filter((name) => !radcnExports.includes(name))
const extraExports = radcnExports.filter((slug) => !uiNames.includes(slug))
const docsOnlyOutcomes = knownOutcomeSlugs.filter((slug) => docsSlugs.includes(slug) && !radcnExports.includes(slug))
const blockNames = blocks.map((item) => item.name).sort()
const chartNames = charts.map((item) => item.name).sort()

const firstCluster = chooseFirstCluster({ docsOnlyOutcomes, exampleGroups, missingUi })
const generatedAt = new Date().toISOString().slice(0, 10)

const lines = [
  '# shadcn/ui Parity Inventory',
  '',
  `Generated by \`node scripts/audit-shadcn-parity.mjs\` on ${generatedAt}.`,
  '',
  'This file is generated from local repository state. Re-run the script after',
  'changing the vendored shadcn checkout, RadCN package exports, or docs',
  'registry.',
  '',
  '## Sources',
  '',
  `- Upstream UI registry: \`${relative(uiRegistryPath)}\``,
  `- Upstream examples registry: \`${relative(examplesRegistryPath)}\``,
  `- Upstream blocks registry: \`${relative(blocksRegistryPath)}\``,
  `- Upstream charts registry: \`${relative(chartsRegistryPath)}\``,
  `- RadCN package manifest: \`${relative(radcnPackagePath)}\``,
  `- RadCN docs registry: \`${relative(docsContentPath)}\``,
  '',
  '## Summary',
  '',
  '| Surface | Count |',
  '| --- | ---: |',
  `| shadcn/ui registry UI items | ${uiNames.length} |`,
  `| shadcn/ui registry examples | ${examples.length} |`,
  `| shadcn/ui registry blocks | ${blocks.length} |`,
  `| shadcn/ui registry chart examples | ${charts.length} |`,
  `| RadCN public package subpaths | ${radcnExports.length} |`,
  `| RadCN docs routes | ${docsSlugs.length} |`,
  '',
  '## Upstream UI Components',
  '',
  '| shadcn slug | RadCN export | Docs route | Outcome |',
  '| --- | --- | --- | --- |',
  ...uiNames.map((name) => {
    let hasExport = radcnExports.includes(name)
    let hasDocs = docsSlugs.includes(name)
    let outcome = hasExport ? 'package-api' : hasDocs ? 'docs-only gap' : 'needs outcome'
    return `| ${name} | ${yesNo(hasExport)} | ${yesNo(hasDocs)} | ${outcome} |`
  }),
  '',
  '## Missing Upstream UI Package APIs',
  '',
  ...bulletList(missingUi, 'None.'),
  '',
  '## RadCN Package Exports Without Upstream UI Counterpart',
  '',
  'These exports may be Remix 3 helpers, documented divergences, or RadCN-specific',
  'package structure. They still need parity decisions where upstream has related',
  'examples or blocks.',
  '',
  ...bulletList(extraExports, 'None.'),
  '',
  '## Known Docs-Only Outcomes',
  '',
  'These are documented in RadCN but are not current package exports.',
  '',
  ...bulletList(docsOnlyOutcomes, 'None.'),
  '',
  '## Upstream Examples By Inferred Component',
  '',
  '| Component | Example count | Examples |',
  '| --- | ---: | --- |',
  ...Object.keys(exampleGroups).sort().map((slug) => {
    let names = exampleGroups[slug].map((item) => item.name).sort()
    return `| ${slug} | ${names.length} | ${names.map((name) => `\`${name}\``).join(', ')} |`
  }),
  '',
  '## Upstream Blocks Queue',
  '',
  '| Block | Categories | Registry dependencies |',
  '| --- | --- | --- |',
  ...blockNames.map((name) => {
    let item = blocks.find((block) => block.name === name)
    return `| ${name} | ${formatList(item.categories)} | ${formatList(item.registryDependencies)} |`
  }),
  '',
  '## Upstream Chart Examples Queue',
  '',
  '| Chart example | Categories | Registry dependencies |',
  '| --- | --- | --- |',
  ...chartNames.map((name) => {
    let item = charts.find((chart) => chart.name === name)
    return `| ${name} | ${formatList(item.categories)} | ${formatList(item.registryDependencies)} |`
  }),
  '',
  '## First Recommended Cluster',
  '',
  `**Cluster:** ${firstCluster.title}`,
  '',
  firstCluster.reason,
  '',
  'Expected next experiment:',
  '',
  ...firstCluster.steps.map((step) => `- ${step}`),
  '',
  '## Notes',
  '',
  '- npm publishing remains out of scope. Install copy stays aspirational.',
  '- `vendor/` is used only as a reference source for this audit.',
  '- Parity means equivalent user-facing behavior, accessibility, visual',
  '  modifiability, and documentation, not literal React or DOM equivalence.',
  '',
]

await writeFile(outputPath, `${lines.join('\n')}\n`)
console.log(`wrote ${relative(outputPath)}`)

function parseRegistryItems(text) {
  let starts = []
  let lines = text.split('\n')
  let offset = 0
  for (let line of lines) {
    if (line === '  {') starts.push(offset)
    offset += line.length + 1
  }

  return starts
    .map((start, index) => text.slice(start, starts[index + 1] ?? text.indexOf('\n]', start)))
    .map(parseItem)
    .filter((item) => item.name)
    .sort((a, b) => a.name.localeCompare(b.name))
}

function parseItem(block) {
  return {
    name: matchString(block, 'name'),
    type: matchString(block, 'type'),
    dependencies: matchArray(block, 'dependencies'),
    registryDependencies: matchArray(block, 'registryDependencies'),
    categories: matchArray(block, 'categories'),
  }
}

function matchString(text, key) {
  return text.match(new RegExp(`${key}: "([^"]+)"`))?.[1] ?? ''
}

function matchArray(text, key) {
  let match = text.match(new RegExp(`${key}: \\[([\\s\\S]*?)\\]`))
  if (!match) return []
  return Array.from(match[1].matchAll(/"([^"]+)"/g), (item) => item[1]).sort()
}

function exportedSlugs(exportsMap) {
  return Object.keys(exportsMap)
    .filter((subpath) => !['.', './styles', './package.json'].includes(subpath))
    .map((subpath) => subpath.replace(/^\.\//, ''))
    .sort()
}

function docsRegistrySlugs(text) {
  return Array.from(
    new Set(Array.from(text.matchAll(/slug: '([^']+)'[\s\S]{0,220}?category: /g), (match) => match[1])),
  ).sort()
}

function groupByDependency(items, knownSlugs) {
  let groups = {}
  for (let item of items) {
    let slug = inferPrimarySlug(item, knownSlugs)
    groups[slug] ??= []
    groups[slug].push(item)
  }
  return groups
}

function inferPrimarySlug(item, knownSlugs) {
  if (item.name.startsWith('chart-')) return 'chart'
  if (item.name.startsWith('typography-')) return 'typography'
  let prefix = knownSlugs.find((slug) => item.name === slug || item.name.startsWith(`${slug}-`))
  if (prefix) return prefix
  if (item.registryDependencies.length > 0) return item.registryDependencies[0]
  return item.name.replace(/-(demo|default|destructive|outline|secondary|example)$/, '')
}

function chooseFirstCluster({ docsOnlyOutcomes, exampleGroups, missingUi }) {
  if (missingUi.includes('form')) {
    return {
      title: 'Form parity and Remix 3 form recipes',
      reason:
        '`form` is the only upstream `ui/` component missing from RadCN package exports, and the upstream registry has a large form example family. It should be resolved before recipe/block work depends on a stable field/form story.',
      steps: [
        'Audit upstream `ui/form.tsx` and form examples by family.',
        'Decide whether RadCN needs a package `radcn/form` API, docs-only recipes, or both.',
        'Implement the chosen form outcome with docs and tests.',
      ],
    }
  }

  if (docsOnlyOutcomes.length > 0) {
    return {
      title: `Docs-only outcomes: ${docsOnlyOutcomes.join(', ')}`,
      reason:
        'These outcomes are already visible in the docs as gaps, so they are the most user-facing parity work left after package UI parity.',
      steps: docsOnlyOutcomes.map((slug) => `Resolve the ${slug} outcome as a package API, recipe, block, or intentional divergence.`),
    }
  }

  let largestExampleGroup = Object.entries(exampleGroups).sort((a, b) => b[1].length - a[1].length)[0]
  return {
    title: `Example parity for ${largestExampleGroup?.[0] ?? 'component docs'}`,
    reason: 'No missing UI package API was found, so the next risk is example and behavior parity depth.',
    steps: [`Audit upstream examples for ${largestExampleGroup?.[0] ?? 'the largest example group'}.`],
  }
}

function bulletList(items, empty) {
  if (items.length === 0) return [`- ${empty}`]
  return items.map((item) => `- \`${item}\``)
}

function formatList(items = []) {
  return items.length === 0 ? '-' : items.map((item) => `\`${item}\``).join(', ')
}

function yesNo(value) {
  return value ? 'yes' : 'no'
}

function relative(filePath) {
  return path.relative(repo, filePath)
}
